package com.java.data_type;

public class Main {
    public static void main(String[] args) {
//        byte 占用 1 个字节数，范围： -128 ~ 127
        byte a = 127;

//        short 占用 2 个字节数，范围：-32768 ~ 32767
        short b = 32767;

//        int 占用 4 个字节数，范围：-2147483648 ~ 2147483647
        int c = 2147483647;

//        long 占用 8 个字节数，范围：-9223372036854775808 ~ 9223372036854775807
        long d = 9223372036854775807L;

        System.out.println(d);
    }
}
